print("=== TUTORIAL: CÓMO LAVAR LOS PLATOS PASO A PASO ===")
print("1. PREPARACIÓN:")
print("   - Usa una esponja de doble cara (azul para limpieza suave, verde para residuos pegados).")
print("   - Consigue jabón líquido para trastes o una barra biodegradable.")
print("   - Prepara dos recipientes: uno con agua jabonosa y otro con agua limpia para enjuagar.")

print("2. ORDEN DE LAVADO (de menos a más sucio):")
print("   a) Vasos y cubiertos (poca grasa).")
print("   b) Platos y tazones (restos de comida).")
print("   c) Ollas y sartenes (grasa endurecida).")

print("3. TÉCNICA DE LAVADO:")
print("Sumerge la esponja en agua jabonosa y escurre el exceso.")
print("Frota en círculos, empezando por el centro del plato hacia los bordes.")
print("Para comida pegada, usa el lado verde de la esponja con un poco de fuerza.")
print("No olvides lavar la base de los platos y los mangos de los cubiertos!")

print("4.ENJUAGUE:")
print("   - Sumerge cada plato en el agua limpia para quitar el jabón.")
print("   - Si tienes grifo, enjuaga bajo el chorro de agua fría.")

print("5. SECADO:")
print("   - Coloca los platos en un escurridor con ranuras para que escurran.")
print("   - Para evitar manchas, sécalos con un trapo limpio de algodón (opcional).")
print("   - Guarda los platos secos en el armario.")

print("¡LISTO! PLATOS LIMPIOS Y BIEN CUIDADOS")